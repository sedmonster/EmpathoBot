# States for the conversation
FIRST, SECOND = range(2)


def start_talk(update, context):
    update.message.reply_text(
        '''Let's have a meaningful conversation.
    Share your thoughts, feelings, or anything you'd like to discuss. 
    I'm here to listen and provide support.Feel free to open up and start our chat. Your words are important to me!'''
    )
    return FIRST


def generate_response(message):
    blob = TextBlob(message)
    sentiment = blob.sentiment.polarity

    if sentiment > 0:
        response = "I'm glad to hear that you're feeling positive!"
    elif sentiment < 0:
        response = "I'm sorry to hear that you're feeling negative. Is there anything I can do to help?"
    else:
        response = "Thank you for sharing. How can I assist you further?"

    return response


def receive_message(update, context):
    user_message = update.message.text
    response = generate_response(user_message)
    update.message.reply_text(response)
    return ConversationHandler.END


conv_handler = ConversationHandler(
    entry_points=[CommandHandler('talk', start_talk)],
    states={
        FIRST: [MessageHandler(filters.TEXT & filters.Command, receive_message)],
    },
    fallbacks=[]
)


